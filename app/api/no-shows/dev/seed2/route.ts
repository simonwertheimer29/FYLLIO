// app/api/no-shows/dev/seed2/route.ts
// Seed multiclinica en 5 bloques.
// GET /api/no-shows/dev/seed2?bloque=1..5
// "Staff ID" y "Sillón ID" son campos computados → no los seteamos.
// Usamos Nombre único por registro para lookup en bloques posteriores.

import { NextResponse } from "next/server";
import { DateTime } from "luxon";
import { base } from "../../../../lib/airtable";

const ZONE   = "Europe/Madrid";
const MARKER = "SEED2_2026";

// ── Helpers ────────────────────────────────────────────────────────────────

function sleep(ms: number) { return new Promise(r => setTimeout(r, ms)); }

function firstStr(x: unknown): string {
  if (typeof x === "string") return x;
  if (Array.isArray(x) && typeof x[0] === "string") return x[0];
  return "";
}

/** Create a single record. */
async function createOne(tabla: string, fields: Record<string, unknown>): Promise<string> {
  const r = await (base(tabla as any).create(fields as any) as any);
  return r.id;
}

/** Create records in batches of 10 (Airtable limit). */
async function createBatch(tabla: string, fieldsList: Record<string, unknown>[]): Promise<string[]> {
  const ids: string[] = [];
  for (let i = 0; i < fieldsList.length; i += 10) {
    const batch = fieldsList.slice(i, i + 10);
    if (batch.length === 1) {
      const r = await (base(tabla as any).create(batch[0] as any) as any);
      ids.push(r.id);
    } else {
      const rs = await (base(tabla as any).create(
        batch.map(f => ({ fields: f })) as any,
      ) as any);
      for (const r of rs) ids.push(r.id);
    }
    if (i + 10 < fieldsList.length) await sleep(250);
  }
  return ids;
}

/**
 * Build { logicalId → airtableRecordId } from any table.
 * Uses "Clínica ID" / "Staff ID" / "Sillón ID" as keys.
 */
async function fetchIdMap(tabla: string, idField: string): Promise<Map<string, string>> {
  const recs = await (base(tabla as any).select({ fields: [idField] }).all() as any);
  const map = new Map<string, string>();
  for (const r of recs) {
    const val = firstStr(r.fields[idField]);
    if (val) map.set(val, r.id);
  }
  return map;
}

/**
 * Build { Nombre → { recId, autoId } } from Staff.
 * autoId = the "Staff ID" value auto-generated by Airtable.
 */
async function fetchStaffByName(): Promise<Map<string, { recId: string; autoId: string }>> {
  const recs = await (base("Staff" as any).select({ fields: ["Nombre", "Staff ID"] }).all() as any);
  const map = new Map<string, { recId: string; autoId: string }>();
  for (const r of recs) {
    const nombre = firstStr(r.fields["Nombre"]);
    const autoId = firstStr(r.fields["Staff ID"]);
    if (nombre) map.set(nombre, { recId: r.id, autoId });
  }
  return map;
}

/** Build { Nombre → recId } from Sillones. */
async function fetchSillonesByName(): Promise<Map<string, string>> {
  const recs = await (base("Sillones" as any).select({ fields: ["Nombre"] }).all() as any);
  const map = new Map<string, string>();
  for (const r of recs) {
    const nombre = firstStr(r.fields["Nombre"]);
    if (nombre) map.set(nombre, r.id);
  }
  return map;
}

