// ── BLOQUE 4: Historial de citas (8 semanas) ───────────────────────────────

async function bloque4() {
  const clinicaMap  = await fetchIdMap("Clínicas", "Clínica ID");
  const staffByName = await fetchStaffByName();
  const sillonByName = await fetchSillonesByName();

  const tratRecs  = await (base("Tratamientos" as any).select({ fields: ["Nombre"] }).all() as any);
  const tratIds: string[]   = tratRecs.map((r: any) => r.id);
  const tratNames: string[] = tratRecs.map((r: any) => firstStr(r.fields["Nombre"]) || "Tratamiento");

  const pacRecs = await (base("Pacientes" as any).select({ maxRecords: 30, fields: [] }).all() as any);
  const pacIds: string[] = pacRecs.map((r: any) => r.id);

  if (!pacIds.length) throw new Error("Sin pacientes.");
  if (!tratIds.length) throw new Error("Sin tratamientos.");

  const records: Record<string, unknown>[] = [];
  let globalIdx = 0;

  for (const weekMon of HIST_WEEKS) {
    const monDt = DateTime.fromISO(weekMon, { zone: ZONE });

    for (const clinicDoctors of [CLINICAL_MADRID, CLINICAL_BARCELONA]) {
      const isBarcelona  = clinicDoctors === CLINICAL_BARCELONA;
      const clinicaId    = isBarcelona ? "CLINIC_002" : "CLINIC_001";
      const clinicaRecId = clinicaMap.get(clinicaId);
      if (!clinicaRecId) continue;

      for (const dayOffset of HIST_DAYS) {
        const dayDt = monDt.plus({ days: dayOffset });

        for (const doctorNombre of clinicDoctors) {
          const staffInfo   = staffByName.get(doctorNombre);
          const sillonNom   = DOC_SILLON[doctorNombre];
          const sillonRecId = sillonNom ? sillonByName.get(sillonNom) : undefined;
          if (!staffInfo) continue;

          const staffMeta = STAFF_DATA.find(s => s.nombre === doctorNombre)!;

          for (const hour of HIST_SLOTS_HOURS) {
            const startDt = dayDt.set({ hour, minute: 0, second: 0 });
            const durMin  = hour === 9 ? 60 : 45;
            const endDt   = startDt.plus({ minutes: durMin });

            const estado   = histEstado(staffMeta.noShowRate, globalIdx);
            const isNoShow = estado === "Cancelado";
            const tratIdx  = globalIdx % tratIds.length;
            const pacIdx   = globalIdx % pacIds.length;

            const fields: Record<string, unknown> = {
              "Nombre":      `${tratNames[tratIdx]} · Paciente ${pacIdx + 1}`,
              "Hora inicio": startDt.toUTC().toISO(),
              "Hora final":  endDt.toUTC().toISO(),
              "Estado":      estado,
              "Notas":       isNoShow ? `[NO_SHOW] ${MARKER}` : MARKER,
              "Clínica":     [clinicaRecId],
              "Profesional": [staffInfo.recId],
              "Tratamiento": [tratIds[tratIdx]],
              "Paciente":    [pacIds[pacIdx]],
            };
            if (sillonRecId) fields["Sillón"] = [sillonRecId];
            records.push(fields);
            globalIdx++;
          }
        }
      }
    }
  }

  const ids = await createBatch("Citas", records);
  return {
    bloque: 4,
    created: ids.length,
    semanas: HIST_WEEKS.length,
    tratamientos: [...new Set(tratNames)],
  };
}

// ── BLOQUE 5: Agenda activa (2 semanas futuras) ────────────────────────────

async function bloque5() {
  const clinicaMap   = await fetchIdMap("Clínicas", "Clínica ID");
  const staffByName  = await fetchStaffByName();
  const sillonByName = await fetchSillonesByName();

  const tratRecs  = await (base("Tratamientos" as any).select({ fields: ["Nombre"] }).all() as any);
  const tratIds: string[]   = tratRecs.map((r: any) => r.id);
  const tratNames: string[] = tratRecs.map((r: any) => firstStr(r.fields["Nombre"]) || "Tratamiento");

  const pacRecs = await (base("Pacientes" as any).select({ maxRecords: 30, fields: [] }).all() as any);
  const pacIds: string[] = pacRecs.map((r: any) => r.id);

  if (!pacIds.length) throw new Error("Sin pacientes.");
  if (!tratIds.length) throw new Error("Sin tratamientos.");

  const records: Record<string, unknown>[] = [];
  let globalIdx = 0;
  let skipped   = 0;

  for (let weekIdx = 0; weekIdx < FUTURE_WEEKS.length; weekIdx++) {
    const monDt = DateTime.fromISO(FUTURE_WEEKS[weekIdx], { zone: ZONE });

    for (const clinicDoctors of [CLINICAL_MADRID, CLINICAL_BARCELONA]) {
      const isBarcelona  = clinicDoctors === CLINICAL_BARCELONA;
      const clinicaId    = isBarcelona ? "CLINIC_002" : "CLINIC_001";
      const clinicaRecId = clinicaMap.get(clinicaId);
      if (!clinicaRecId) continue;

      let slotInClinic = 0;

      for (const dayOffset of FUTURE_DAYS) {
        const dayDt = monDt.plus({ days: dayOffset });

        for (const doctorNombre of clinicDoctors) {
          const staffInfo   = staffByName.get(doctorNombre);
          const sillonNom   = DOC_SILLON[doctorNombre];
          const sillonRecId = sillonNom ? sillonByName.get(sillonNom) : undefined;
          if (!staffInfo) continue;

          for (const hour of FUTURE_SLOTS_HOURS) {
            // Skip 1 of 15 → ~93% fill (close to 80% before cancellados)
            if (slotInClinic % 15 === 7) { slotInClinic++; skipped++; continue; }

            const startDt = dayDt.set({ hour, minute: 0, second: 0 });
            const durMin  = hour === 9 ? 60 : 45;
            const endDt   = startDt.plus({ minutes: durMin });

            const estado  = futureEstado(weekIdx, globalIdx);
            const tratIdx = globalIdx % tratIds.length;
            const pacIdx  = globalIdx % pacIds.length;

            const fields: Record<string, unknown> = {
              "Nombre":      `${tratNames[tratIdx]} · Paciente ${pacIdx + 1}`,
              "Hora inicio": startDt.toUTC().toISO(),
              "Hora final":  endDt.toUTC().toISO(),
              "Estado":      estado,
              "Notas":       MARKER,
              "Clínica":     [clinicaRecId],
              "Profesional": [staffInfo.recId],
              "Tratamiento": [tratIds[tratIdx]],
              "Paciente":    [pacIds[pacIdx]],
            };
            if (sillonRecId) fields["Sillón"] = [sillonRecId];
            records.push(fields);
            globalIdx++;
            slotInClinic++;
          }
        }
      }
    }
  }

  const ids = await createBatch("Citas", records);
  return { bloque: 5, created: ids.length, skipped };
}

// ── Router ─────────────────────────────────────────────────────────────────

export async function GET(req: Request) {
  const { searchParams } = new URL(req.url);
  const bloque = searchParams.get("bloque") ?? "";
  try {
    switch (bloque) {
      case "1": return NextResponse.json(await bloque1());
      case "2": return NextResponse.json(await bloque2());
      case "3": return NextResponse.json(await bloque3());
      case "4": return NextResponse.json(await bloque4());
      case "5": return NextResponse.json(await bloque5());
      default:
        return NextResponse.json({ error: "Parámetro bloque requerido: 1..5" }, { status: 400 });
    }
  } catch (e: any) {
    console.error(`[seed2 bloque ${bloque}]`, e);
    return NextResponse.json({ error: e?.message ?? "Error" }, { status: 500 });
  }
}
